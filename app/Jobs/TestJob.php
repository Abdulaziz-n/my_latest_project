     * Create a new job instance.
     *
     * @return void
     */
    public function __construct($request)
    {
        $this->request = $request;
    }

    /**
     * Execute the job.
     *
     * @return void
     */
    public function handle($request)
    {
        $this->create();
    }

    public function create()
    {
        return $this->request;
    }
}
